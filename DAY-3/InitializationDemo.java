public class InitializationDemo {
    public static void main(String[] args) {
        int number;
        double salary;
        boolean TF;
        char grade;

        number = 100;
        salary = 50000.50;
        TF = true;
        grade = 'A';

        System.out.print("Number (Integer): " + number +
                "\nSalary (Double): " + salary +
                "\nTrue or False (Boolean): " + TF +
                "\nGrade (Char): " + grade);
    }
}
